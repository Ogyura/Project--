"""
Задача 1.
Составить функцию, которая выполнит суммирования числового ряда.
(Функция для суммирования чисел от 1 до N)
"""

def sum_series(N):
    """Функция для суммирования чисел от 1 до N"""
    total = 0
    i = 1
    while i <= N:
        total += i
        i += 1
    return total

# Пример использования
n = int(input("Введите N для суммирования: "))
result = sum_series(n)
print(f"Сумма чисел от 1 до {n}: {result}")