"""
Задача 2.
Описать функцию SortDec3(A, B, C), меняющую содержимое переменных A, B, C
таким образом, чтобы их значения оказались упорядоченными по убыванию.
С помощью этой функции упорядочить по убыванию два данных набора из трех чисел.
"""

def SortDec3(A, B, C):
    """Упорядочивает три числа по убыванию"""
    # Сортируем путем попарных сравнений
    if A < B:
        A, B = B, A
    if A < C:
        A, C = C, A
    if B < C:
        B, C = C, B
    return A, B, C

# Первый набор
print("Первый набор чисел:")
A1 = float(input("A1: "))
B1 = float(input("B1: "))
C1 = float(input("C1: "))

A1, B1, C1 = SortDec3(A1, B1, C1)
print(f"После сортировки: {A1}, {B1}, {C1}")

# Второй набор
print("\nВторой набор чисел:")
A2 = float(input("A2: "))
B2 = float(input("B2: "))
C2 = float(input("C2: "))

